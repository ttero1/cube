#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	flag;
	int	res;

	i = 0;
	res = 0;
	flag = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		flag *= -1;
		i++;
	}
	if (str[i] == '+' && flag == -1)
		return (0);
	if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res *= 10;
		res += str[i] - '0';
		i++;
	}
	return (res * flag);
}
