#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	if (lst == 0)
		return (0);
	i = 1;
	while (lst->next != NULL)
	{
		lst = lst->next;
		i++;
	}
	return (i);
}
