#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	x;

	x = c;
	i = 0;
	while (s[i])
	{
		if (s[i] == x)
			return ((char *)(s + i));
		i++;
	}
	if (s[i] == x)
		return ((char *)(s + i));
	return (0);
}
