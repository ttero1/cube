#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t size)
{
	unsigned long	i;
	size_t			len;

	i = 0;
	len = ft_strlen(src);
	if (size != 0)
	{
		while (src[i] && i < size - 1)
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (len);
}
